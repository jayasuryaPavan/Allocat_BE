package com.allocat.pos.enums;

/**
 * Enum representing the type of discount
 */
public enum DiscountType {
    /**
     * Percentage-based discount (e.g., 10% off)
     */
    PERCENTAGE,

    /**
     * Fixed amount discount (e.g., $5 off)
     */
    FIXED_AMOUNT,

    /**
     * Buy X get Y promotion (e.g., Buy 2 Get 1 Free)
     */
    BUY_X_GET_Y
}
